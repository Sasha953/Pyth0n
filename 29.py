#Змініть функцію з попереднього завдання так, щоб з її допомогою можна було розраховувати вага для різної кількості років, наприклад 5 або 20 років.
#Нехай ця функція приймає три аргументи: початкову вагу, прибавку ваги в рік і кількість років.
a = input("Ваш вес: ").split()
b = input("На сколько будет увеличиваться вас вес в год: ").split()
c = input("Сколько лет: ").split()
a1 = int(a[0])
b1 = int(b[0])
c1 = int(c[0])
d = a1 + b1 * c1

print("К концу экспедиции ваш вес составит:", d)